class Question:
    def __init__(self, q_text, q_answer):
        # Constructor method to initialize the attributes of the Question class
        # q_text: The text of the question
        # q_answer: The correct answer to the question
        self.text = q_text  # Assign the question text to the 'text' attribute
        self.answer = q_answer  # Assign the correct answer to the 'answer' attribute



